#include <stdio.h>
#include <stdlib.h>

int main() {

	int cont = 0;

	printf("Tabuada do 7 ");

	for (cont = 0; cont <= 10; cont++) {
		printf("\n\n%d x 7 = %d", cont, cont * 7);
	}

	printf("\n\n-------------------------------------------------------------");

	printf("\n\nTabuada do 8");

	for (cont = 0; cont <= 10; cont++) {
		printf("\n\n%d x 8 = %d", cont, cont * 8);
	}

	printf("\n\n");


	system("pause");

}
